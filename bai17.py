a=int(input('Nhập số nguyên thứ nhất: '))
b=int(input('Nhập số nguyên thứ hai: '))
c=int(input('Nhập số nguyên thứ ba: '))
solonnhat=max(a,b,c)
print(f'Số lớn nhất trong ba số nguyên {a}, {b}, {c} là: {solonnhat}')
sonhonhat=min(a,b,c)
print(f'Số nhỏ nhất trong ba số nguyên {a}, {b}, {c} là: {sonhonhat}')

