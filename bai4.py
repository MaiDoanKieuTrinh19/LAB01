N=int(input('Nhập số nguyên dương có 2 chữ số N= '))
if 10<=N<100:
    tongN=N//10+N%10
    print(f'Tổng 2 chữ số của N là: {tongN}')
else:
    print('vui lòng nhập số nguyên dương có 2 chữ số')
    


